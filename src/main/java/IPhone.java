public class IPhone {

    //  아래와 같은 기능 (메서드) 을 가진 IPhone 클래스.

    public void turnOn() {
        System.out.println("아이폰 전원 ON");
    }

    public void turnOff() {
        System.out.println("아이폰 전원 OFF");
    }

    public void soundUp() {
        System.out.println("아이폰 볼륨 UP");
    }

    public void soundDown() {
        System.out.println("아이폰 볼륨 DOWN");
    }

}   //  IPhone